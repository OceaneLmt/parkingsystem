	@Mock
	private static ParkingSpotDAO parkingSpotDAO = new ParkingSpotDAO();

	@BeforeEach
	private void setUpPerTest() {
		ticket = new Ticket();
		fareCalculatorService = new FareCalculatorService();
	}

	/**
	 * Set up for calculateFareCarRecurringCustomer test
	 */
	private void setUpMockito() {
		try {
			when(parkingSpotDAO.getRowsCountWithSameVehiculeNumber(null)).thenReturn(1);
		} catch (Exception e) {
			e.printStackTrace();
			throw new RuntimeException("Failed to set up test mock objects");
		}

	}

	@Test
	public void calculateFareCar() {
		Date inTime = new Date();
		inTime.setTime(System.currentTimeMillis() - (60 * 60 * 1000));
		Date outTime = new Date();
		ParkingSpot parkingSpot = new ParkingSpot(1, ParkingType.CAR, false);

		ticket.setInTime(inTime);
		ticket.setOutTime(outTime);
		ticket.setParkingSpot(parkingSpot);
		fareCalculatorService.calculateFare(ticket);
		assertEquals(Fare.CAR_RATE_PER_HOUR, ticket.getPrice());
	}

	/**
	 * Should give a 5% discount on parking fare
	 */
	@Test
	public void calculateFareCarRecurringCustomer() {
		setUpMockito();
		fareCalculatorService.setParkingSpotDAO(parkingSpotDAO);
		Date inTime = new Date();
		inTime.setTime(System.currentTimeMillis() - (60 * 60 * 1000));
		Date outTime = new Date();
		ParkingSpot parkingSpot = new ParkingSpot(1, ParkingType.CAR, false);

		ticket.setInTime(inTime);
		ticket.setOutTime(outTime);
		ticket.setParkingSpot(parkingSpot);
		fareCalculatorService.calculateFare(ticket);
		assertEquals(ticket.getPrice(), Fare.CAR_RATE_PER_HOUR * (5d / 100));
	}

